function normalize(t){ return t.toLowerCase().replace(/[?,.!:;„""()\/]/g,' ').replace(/\s+/g,' ').trim(); }
function tokens(t){ return normalize(t).split(' ').filter(w=>w.length>2); }
function answerQuery(qRaw){
  const qt=tokens(qRaw); if(!qt.length) return null;
  let best=null,bestScore=0;
  ASSIST_DOCS.forEach(d=>{
    const hay=normalize(d.key), hq=normalize(d.q);
    let sc=0;
    qt.forEach(w=>{ if(hq.includes(w)) sc+=3; else if(hay.includes(w)) sc+=1; });
    if(sc>bestScore){ bestScore=sc; best=d; }
  });
  return bestScore>=2 ? best : null;
}
