function renderBlocks(blocks){ return (blocks||[]).map(renderBlock).join(''); }
function renderBlock(b){
  switch(b.type){
    case 'lead': return `<p class="sec-lead">${b.text}</p>`;
    case 'h': return `<h3 class="block-h">${esc(b.text)}</h3>`;
    case 'prose': return `<div class="block prose study-hide">${b.items.map(p=>`<p>${p}</p>`).join('')}</div>`;
    case 'cards': return `<div class="block study-hide"><div class="card-grid">${b.items.map(c=>`
        <div class="card">
          ${c.color?`<span class="card-num" style="color:${c.color}">${esc(c.n||'')}</span>`:`<span class="card-num">${esc(c.n||'')}</span>`}
          <div class="card-title">${esc(c.title)}</div>
          <div class="card-body">${c.body}</div>
          ${c.tag?`<span class="card-tag">${esc(c.tag)}</span>`:''}
        </div>`).join('')}</div></div>`;
    case 'numbers': return `<div class="block"><div class="numbers">${b.items.map(n=>`
        <div class="num-chip"><div class="num-val mono">${esc(n.val)}</div><div class="num-label">${esc(n.label)}</div></div>`).join('')}</div></div>`;
    case 'facts': return `<div class="block"><ul class="facts">${b.items.map(f=>`<li>${f}</li>`).join('')}</ul></div>`;
    case 'mistakes': return `<div class="block"><ul class="mistakes">${b.items.map(f=>`<li>${f}</li>`).join('')}</ul></div>`;
    case 'steps': return `<div class="block"><div class="steps">${b.items.map(s=>`<div class="step"><span>${s}</span></div>`).join('')}</div></div>`;
    case 'table': return `<div class="block"><div class="table-wrap"><table>
        <thead><tr>${b.head.map(h=>`<th>${esc(h)}</th>`).join('')}</tr></thead>
        <tbody>${b.rows.map(r=>`<tr>${r.map((c,i)=>`<td${i>0?' class="mono"':''}>${esc(c)}</td>`).join('')}</tr>`).join('')}</tbody>
      </table></div></div>`;
    case 'note': return `<div class="block note study-hide">${b.text}</div>`;
    case 'summary': return `<div class="block summary">${b.text}</div>`;
    case 'timeline': return `<div class="block study-hide"><div class="timeline">${b.items.map(t=>`
        <div class="tl-item"><div class="tl-year mono">${esc(t.year)}</div><div class="tl-text">${esc(t.text)}</div></div>`).join('')}</div></div>`;
    case 'accordion': return `<div class="block study-hide">${b.items.map((it,i)=>`
        <div class="acc" data-acc>
          <button class="acc-head"><span class="acc-ico">${it.ico||'•'}</span>${esc(it.title)}<span class="acc-arrow">›</span></button>
          <div class="acc-body"><div class="acc-inner">${renderBlocks(it.content)}</div></div>
        </div>`).join('')}</div>`;
    default: return '';
  }
}
