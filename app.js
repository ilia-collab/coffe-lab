/* ---------- SEARCH INDEX ---------- */
function blockText(blocks){
  let t='';
  (blocks||[]).forEach(b=>{
    if(b.text) t+=' '+b.text;
    if(b.items) b.items.forEach(it=>{
      if(typeof it==='string') t+=' '+it;
      else { t+=' '+Object.values(it).filter(v=>typeof v==='string').join(' '); if(it.content) t+=' '+blockText(it.content); }
    });
    if(b.head) t+=' '+b.head.join(' ');
    if(b.rows) t+=' '+b.rows.flat().join(' ');
  });
  return t;
}
const SEARCH_INDEX = CONTENT.map(s=>({
  id:s.id, title:s.title, cat:s.cat,
  text:(s.title+' '+(s.lead||'')+' '+blockText(s.blocks)).replace(/<[^>]+>/g,' ').replace(/\s+/g,' ')
}));
function stripTags(t){ return t.replace(/<[^>]+>/g,' '); }

function runSearch(q){
  const box=$('#searchResults');
  q=q.trim().toLowerCase();
  if(q.length<2){ box.classList.remove('show'); box.innerHTML=''; return; }
  const res=[];
  SEARCH_INDEX.forEach(d=>{
    const hay=d.text.toLowerCase();
    const idx=hay.indexOf(q);
    let score=idx<0?-1:1000-idx;
    if(d.title.toLowerCase().includes(q)) score+=2000;
    if(score>0){
      let snip='';
      if(idx>=0){ const st=Math.max(0,idx-35); snip='…'+stripTags(d.text).slice(st,idx+q.length+55)+'…'; }
      res.push({...d,score,snip});
    }
  });
  res.sort((a,b)=>b.score-a.score);
  if(!res.length){ box.innerHTML=`<div class="sr-empty">ვერაფერი მოიძებნა „${esc(q)}"-ზე</div>`; box.classList.add('show'); return; }
  const hi=t=>esc(t).replace(new RegExp('('+q.replace(/[.*+?^${}()|[\]\\]/g,'\\$&')+')','ig'),'<mark>$1</mark>');
  box.innerHTML=res.slice(0,8).map((r,i)=>`
    <div class="sr-item${i===0?' sel':''}" data-id="${r.id}">
      <div class="sr-cat">${CAT_LABEL[r.cat]||''}</div>
      <div class="sr-title">${esc(r.title)}</div>
      <div class="sr-snip">${hi(r.snip)}</div>
    </div>`).join('');
  box.classList.add('show');
  $$('.sr-item',box).forEach(it=>it.onmousedown=e=>{ e.preventDefault(); go(it.dataset.id); $('#globalSearch').value=''; box.classList.remove('show'); });
}

